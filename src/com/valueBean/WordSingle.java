package com.valueBean;

public class WordSingle {
	private int id;
	private String wordTime;
	private String wordContent;
	private String wordPicsrc;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public String getWordTime() {
		return wordTime;
	}
	public void setWordTime(String wordTime) {
		this.wordTime = wordTime;
	}
	
	public String getWordContent() {
		return wordContent;
	}
	public void setWordContent(String wordContent) {
		this.wordContent = wordContent;
	}
	
	public String getWordPicsrc() {
		return wordPicsrc;
	}
	public void setWordPicsrc(String wordPicsrc) {
		this.wordPicsrc = wordPicsrc;
	}
}
